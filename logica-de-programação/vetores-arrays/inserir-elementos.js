let numbers = [1, 3, 4, 5, 6, 7, 9] 
console.log('Array:')
console.log(numbers)

numbers.unshift(10)
console.log(`Inserindo elemento 10 no inicio do array:`)
console.log(numbers)


numbers.push(12)
console.log(`Inserindo elemento 12 no final do array:`)
console.log(numbers)

