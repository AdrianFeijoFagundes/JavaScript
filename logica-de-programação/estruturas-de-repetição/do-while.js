/* 
    WHILE
    
    no FINAL da estrutura WHILE é executado um teste lógico, se o teste for TRUE o bloco é executado enquanto o teste lógico for TRUE, quando o teste lógico resultar em FALSE o fluxo de comandos continua normalmente.

    Exemplo contar até 10 
*/
let contador = 0
do {
    console.log(contador)
    contador++
} while(contador <= 10)